package com.ct.erp.list.model;


public class PageInfo {

	private Select select;
	
	private CurInput curInput;
	
	private Button upButton;
	
	private Button downButton;
	
	private Button goButton;
	

	public Button getGoButton() {
		return goButton;
	}

	public void setGoButton(Button goButton) {
		this.goButton = goButton;
	}

	public Select getSelect() {
		return select;
	}

	public void setSelect(Select select) {
		this.select = select;
	}

	public CurInput getCurInput() {
		return curInput;
	}

	public void setCurInput(CurInput curInput) {
		this.curInput = curInput;
	}

	public Button getUpButton() {
		return upButton;
	}

	public void setUpButton(Button upButton) {
		this.upButton = upButton;
	}

	public Button getDownButton() {
		return downButton;
	}

	public void setDownButton(Button downButton) {
		this.downButton = downButton;
	}
	
	
	
	
}
